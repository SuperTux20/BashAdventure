#!/bin/bash

spittext(){
    printf "%*s" $(((42+$COLUMNS)/2)) "$1"
    printf "%*s\n" $((($COLUMNS-42)/2))
    sleep .125
}
declare -a text=(
    "   ____       __    ____    ____ _________ "
    "  / __ \     /  \   |   \  /   | |  ______|"
    " / /  \_\   /    \  | |\ \/ /| | | |       "
    "/ /        /  /\  \ | | \  / | | | |       "
    "| |   ____ | |  | | | |  \/  | | | |____   "
    "| |  |__ | | |__| | | |      | | |  ____|  "
    "\ \    | | |  __  | | |      | | | |       "
    " \ \__/ /  | |  | | | |      | | | |______ "
    "  \____/   |_|  |_| |_|      |_| |________|"
    "   ____    ___  ___ _________  _________   "
    "  / __ \   | |  | | |  ______| |  _____ \  "
    " / /  \ \  | |  | | | |        | |     \ \ "
    "/ /    \ \ | |  | | | |        | |_____/ / "
    "| |    | | | |  | | | |____    |  __  __/  "
    "| |    | | | |  | | |  ____|   | |  \ \    "
    "\ \    / / \ \  / / | |        | |   \ \   "
    " \ \__/ /   \ \/ /  | |______  | |    \ \  "
    "  \____/     \__/   |________| |_|     \_\ "
)
clear
COLUMNS=$(tput cols)
printf "\e[41m"
for line in "${text[@]}"
do
    spittext "$line"
done
echo -e "\e[0m"